package com.worm.guo.po;

public class WebSite {
	private int websiteNewSiteId; // 站点id
	private String siteName; // 站点名称
	private String siteUrl; // 站点url

	public int getWebsiteNewSiteId() {
		return websiteNewSiteId;
	}

	public void setWebsiteNewSiteId(int websiteNewSiteId) {
		this.websiteNewSiteId = websiteNewSiteId;
	}

	public String getSiteName() {
		return siteName;
	}

	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}

	public String getSiteUrl() {
		return siteUrl;
	}

	public void setSiteUrl(String siteUrl) {
		this.siteUrl = siteUrl;
	}
}
